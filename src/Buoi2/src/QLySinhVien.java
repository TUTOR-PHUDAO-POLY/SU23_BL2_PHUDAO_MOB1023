    public void loadTable(ArrayList<SinhVien> list) {
        dtm.setRowCount(0);//xóa toàn bộ dòng trong table
        for (SinhVien o : list) {
            Object[] rowData = {
                o.getMaSV(),
                o.getTen(),
                o.getTuoi(),
                o.isGioiTinh() == true ? "Nam" : "Nữ",
                o.getDiaChi()
            };
            dtm.addRow(rowData);
        }
    }
    
    public void showDetail(int index) {
        SinhVien sv = list.get(index);
        txtMaSV.setText(sv.getMaSV());
        txtTen.setText(sv.getTen());
        txtTuoi.setText(sv.getTuoi() + "");
        txtDiaChi.setText(sv.getDiaChi());
        boolean gioiTinh = sv.isGioiTinh();
        if (gioiTinh == true) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }
   public void clearForm(){
       txtMaSV.setText("");
       txtTen.setText("");
       txtTuoi.setText("");
       txtDiaChi.setText("");
       rdoNam.setSelected(true);
   }
   public SinhVien getFormData(){
       String ma=txtMaSV.getText().trim();
       String ten=txtTen.getText().trim();
       String tuoi=txtTuoi.getText().trim();
       String diaChi=txtDiaChi.getText().trim();
       boolean gt;
       int tuoi1;
       if(rdoNam.isSelected()==true){
           gt=true;
       }else{
           gt=false;
       }
       if(ma.length()==0){
           JOptionPane.showMessageDialog(this, "Không được để trống mã");
           txtMaSV.requestFocus();
           return null;
       }
       if(ten.length()==0){
           JOptionPane.showMessageDialog(this, "Không được để trống tên");
           txtTen.requestFocus();
           return null;
       }
       if(tuoi.length()==0){
           JOptionPane.showMessageDialog(this, "Không được để trống tuổi");
           txtTuoi.requestFocus();
           return null;
       }else{
           try {
                tuoi1=Integer.parseInt(tuoi);
           } catch (Exception e) {
               e.printStackTrace();
               JOptionPane.showMessageDialog(this, "Sai định dạng tuổi");
               txtTuoi.requestFocus();
               return null;
           }
          
       }
       if(diaChi.length()==0){
           JOptionPane.showMessageDialog(this, "Không được để trống địa chỉ");
           txtDiaChi.requestFocus();
           return null;
       }
       return new SinhVien(ma, ten, tuoi1, diaChi, gt);
   }
