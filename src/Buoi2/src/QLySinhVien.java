    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String ten=txtTimKiem.getText().trim();
        ArrayList<SinhVien> listSVTim=svs.getSVByTen(ten);
        if(ten.length()==0){//nếu như k nhập tên cần tìm thì trả về danh sách getall
            loadTable(list);
        }else{//nếu như nhập thì check xem có tìm thấy hay không
            if(listSVTim.isEmpty()){
                JOptionPane.showMessageDialog(this, "Không tìm thấy sinh viên có tên "+ten);
                
            }else{
                JOptionPane.showMessageDialog(this, "Tìm thấy");
                loadTable(listSVTim);
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row=tblBang.getSelectedRow();//lấy ra dòng đc chọn
        if(row<0){//k có dòng nào đc chọn
            return;
        }
        SinhVien sv=getFormData();
        svs.updateSV(row, sv);
        JOptionPane.showMessageDialog(this, "Update thành công");
        loadTable(list);
        clearForm();
    }//GEN-LAST:event_btnSuaActionPerformed

